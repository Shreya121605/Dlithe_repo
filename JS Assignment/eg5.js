//5]create a simple calculator
let a=10,b=5,operator="+";
switch(operator){
    case '+':console.log(a+b);
    break;
    case '-':console.log(a-b);
    break;
    case '*':console.log(a*b);
    break;
    case '/':console.log(a/b);
    break;
    case '%':console.log(a%b);
    break;
    default:console.log("Invalid Operator");
}
